// Heap
/* 
    - max heap : nilai simpul orang tua harus lebih besar dari anak-anaknya
    - min heap : nilai simput orang tua harus lebih kecil dari anak-anaknya
    - biasa dipakai untuk sorting

    //algoritma max heap
    - membuat simpul baru sebagai simpul akhir
    - memberikan nilai kepada simpul baru
    - membandingkan nilai simpul anak dengan orang tua
    - jika nilai dari simpul orang tua lebih kecil dari simpul anak tukar nilai
    - ulangi langkah 3 dan 4 samap syarat heap terpenuhi

    //algoritma penghapusan pada max heap
    - hapus simpul akar
    - pindahkan elemen terakhir dari level paling rendah dari simpul akar
    - membandingkan nilai simpul anak dengan orang tua
    - jika nilai dari simpul orang tua lebih kecil dari simpul anak tukar nilai
    - ulangi langkah 3 dan 4 samap syarat heap terpenuhi
 */

